    private void continuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continuarActionPerformed
        int num = TFsexo.getSelectedIndex();
        String nome, senha, confirmarSenha, email,telefone,sexo;
        nome = TFnome.getText();
        senha = String.valueOf(TFsenha.getPassword());
        confirmarSenha = String.valueOf(TFsenhaconfirma.getPassword());
        email = TFemail.getText();
        telefone = TFtelefone.getText();
        if(num==0)
            sexo = "M";
        else
            sexo = "F";
        
        if (senha.equals(confirmarSenha)){
            log.nome = nome;
            log.senha = senha;
            log.email = email;
            log.telefone = telefone;
            log.sexo = sexo;
            Login.setCad(true);
            CadastroDAO cadDAO = new CadastroDAO();
            cadDAO.atualizarcli(log);
            
            if(Login.isCad() && Login.isRes()){
                PrincipalCADRES p1 = new PrincipalCADRES();
                p1.setVisible(true);
                this.dispose();}
            else if(Login.isCad()){
                PrincipalCAD p2 = new PrincipalCAD();
                p2.setVisible(true);
                this.dispose();} 
            else{
                Principal1 p3 = new Principal1();
                p3.setVisible(true);
                this.dispose();}
            
        } else {
            JOptionPane.showMessageDialog(null, "A senha e a confirmação são diferentes!");
        }
    }//GEN-LAST:event_continuarActionPerformed

